// if-else statement
var result = 50;

if(result > 40) {
    console.log("You passed the test")
} else {
    console.log("You did not pass the test")
}


// if, else-if, else statement
var place = 'first';

if(place == 'first') {
    console.log('Gold')
} else if (place == 'second') {
    console.log('Silver')
} else {
    console.log('Bronze')
}